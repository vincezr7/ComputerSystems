/* 
 * bitAnd - x&y using only ~ and | 
 *   Example: bitAnd(6, 5) = 4
 *   Legal ops: ~ |
 *   Max ops: 8
 *   Rating: 1
 */
int bitAnd(int x, int y) {

  // p^q is a logical equivalence to ~(~p | ~q)
  // proof begins with p->q = ~p | q

  int nx = ~x;
  int ny = ~y;
  int nxORny = (nx | ny);
  int xAndy = ~nxORny;

  return xAndy;

}
